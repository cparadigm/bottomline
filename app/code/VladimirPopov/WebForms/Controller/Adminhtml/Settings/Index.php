namespace VladimirPopov\WebForms\Controller\Adminhtml\Settings;

use Magento\Backend\App\Action;

class Index extends \Magento\Backend\App\Action
{
    /**
     * {@inheritdoc}
     */
    protected function _isAllowed()
    {
        return $this->_authorization->isAllowed('VladimirPopov_WebForms::settings');
    }

    /**
     * Save action
     *
     * @return \Magento\Framework\Controller\ResultInterface
     */
    public function execute()
    {
        $resultRedirect = $this->resultRedirectFactory->create();

        return $resultRedirect->setPath('adminhtml/system_config/edit',['section'=>'webforms']);
    }
}